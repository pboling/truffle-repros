begin
  generate
  actual = generated("gemfiles/breakfast.gemfile")
  if actual == expected
    puts "PASS no active issue: Appraisal2 DSL generation matched expected output"
  else
    puts "CURRENT ISSUE: generated Appraisal2 DSL output differed"
    puts "--- expected"
    puts expected
    puts "--- actual"
    puts actual
    exit 2
  end
rescue StandardError => e
  puts "CURRENT ISSUE: Appraisal2 DSL generation raised on #{RUBY_ENGINE}"
  puts "#{e.class}: #{e.message}"
  puts e.backtrace.first(12)
  exit 2
end
